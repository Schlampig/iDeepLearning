def main(context, question):
    eval_examples = read_squad_examples(context, question)

    eval_writer = FeatureTransfer()
    eval_features = []
    eval_binary = []

    def append_feature(feature):
        eval_features.append(feature)
        eval_binary.append(eval_writer.process_feature(feature))

    convert_examples_to_features_save(examples=eval_examples,
                                      tokenizer=tokenizer,
                                      max_seq_length=FLAGS.max_seq_length,
                                      doc_stride=FLAGS.doc_stride,
                                      max_query_length=FLAGS.max_query_length,
                                      output_fn=append_feature)

    tf.logging.info("***** Running predictions *****")
    tf.logging.info("  Num orig examples = %d", len(eval_examples))
    tf.logging.info("  Num split examples = %d", len(eval_features))
    tf.logging.info("  Batch size = %d", FLAGS.predict_batch_size)

    result = predictor({"examples": eval_binary})

    all_results = []
    for i in range(len(result.get("unique_ids"))):
        if len(all_results) % 1000 == 0:
            tf.logging.info("Processing example: %d" % (len(all_results)))
        unique_id = int(result["unique_ids"][i])
        start_logits = [float(x) for x in result["start_logits"][i].flat]
        end_logits = [float(x) for x in result["end_logits"][i].flat]
        all_results.append(RawResult(unique_id=unique_id, start_logits=start_logits, end_logits=end_logits))

    result = write_predictions(eval_examples, eval_features, all_results,
                               FLAGS.n_best_size, FLAGS.max_answer_length,
                               FLAGS.do_lower_case)
    return result


